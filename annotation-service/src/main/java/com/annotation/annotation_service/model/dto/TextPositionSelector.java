package com.annotation.annotation_service.model.dto;

public class TextPositionSelector {
    private String type = "TextPositionSelector";

    private int start;
    private int end;

    // Getters and Setters
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
